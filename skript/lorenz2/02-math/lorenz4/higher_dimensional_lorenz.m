## Higher Dimensional Lorenz System
function dx = higher_dimensional_lorenz(X, T, kappa, nu, a, c, T0)
  b01 = X(1);
  ## ---
  b11 = X(2);
  b02 = X(3);
  ## ---
  b21 = X(4);
  b12 = X(5);
  b03 = X(6);
  ## ---
  b31 = X(7);
  b22 = X(8);
  b13 = X(9);
  b04 = X(10);
  ## ---
  a11 = X(11);
  ## ----
  a21 = X(12);
  a12 = X(13);
  ## ---
  a31 = X(14);
  a22 = X(15);
  a13 = X(16);

  order = 4;

  dx = zeros(order^2,1);
  dx(1) = - kappa * b01 ...
          + a/4 * a11 * b12 ...
          + a/4 * a12 * b11 ...
          + a/4 * a12 * b13 ...
          + a/4 * a13 * b12 ...
          + a/2 * a21 * b22 ...
          + a/2 * a22 * b21;
  dx(2) = - (a**2 + 1) * kappa * b11 ...
          + (a * T0) /  pi * a11 ...
          + a * a11 * b02 ...
          - a / 2 * a12 * b01 ...
          - (3 * a) / 2 * a12 * b03 ...
          - (3 * a) / 2 * a12 * b21 ...
          - (3 * a) / 2 * a21 * b12 ...
          - a * a13 * b02 ...
          + a * a13 * b22 ...
          + 2 * a * a13 * b04 ...
          + a * a22 * b13 ...
          + a * a22 * b31 ...
          + a * a31 * b22;
  dx(3) = - 4 * kappa * b02 ...
          - a / 2 * a11 * b11 ...
          - a * a21 * b21 ...
          + a / 2 * a11 * b13 ...
          + a / 2 * a13 * b11 ...
          - (3 * a) / 2 * a31 * b31;
  dx(4) = - (4 * a**2 + 1) * kappa * b21 ...
          + (2 * a * T0)/pi * a21 ...
          + (3 * a)/4 * a11 * b12 ...
          - (3 * a)/4 * a12 * b11 ...
          + 2 * a * a21 * b02 ...
          + (5 * a)/4 * a12 * b13 ...
          - (5 * a)/4 * a13 * b12 ...
          - a * a22 * b01 ...
          + (5 * a)/4 * a12 * b31 ...
          + (5 * a)/4 * a31 * b12 ...
          + 3 * a * a22 * b03;
  dx(5) = - (a**2 + 4) * kappa * b12 ...
          + (a * T0)/pi * a12 ...
          - a/2 * a11 * b01 ...
          + (3 * a)/2 * a11 * b03 ...
          - (3 * a)/4 * a11 * b21 ...
          - (3 * a)/4 * a21 * b11 ...
          - (5 * a)/4 * a21 * b31 ...
          - (5 * a)/4 * a31 * b21 ...
          - (5 * a)/4 * a13 * b21 ...
          + (5 * a)/4 * a21 * b13 ...
          - a/2 * a13 * b01 ...
          + 2 * a * a12 * b04;
  dx(6) = - 9 * kappa * b03 ...
          - (3 * a)/4 * a11 * b12 ...
          - (3 * a)/4 * a12 * b11 ...
          - (3 * a)/2 * a21 * b22 ...
          - (3 * a)/2 * a22 * b21;
  dx(7) = - (9 * a**2 + 1) * kappa * b31 ...
          + (3 * a * T0) / pi * a31 ...
          + (5 * a) / 4 * a21 * b12 ...
          - (5 * a) / 4 * a12 * b21 ...
          + 3 * a * a31 * b02 ...
          + a * a11 * b22 ...
          - a * a22 * b11 ...
          - 2 * a * a13 * b22 ...
          + 2 * a * a22 * b13;
  dx(8) = - (4 * a**2 + 4) * kappa * b22 ...
          + (2 * a * T0) / pi * a22 ...
          - a * a21 * b01 ...
          + 3 * a * a21 * b03 ...
          + a * a11 * b13 ...
          - a * a13 * b11 ...
          - a * a11 * b31 ...
          - a * a31 * b11 ...
          + 2 * a * a13 * b31 ...
          + 2 * a * a31 * b13 ...
          + 4 * a * a22 * b04;
  dx(9) = - (a**2 + 9) * kappa * b13 ...
          + (a * T0) / pi * a13 ...
          - a * a11 * b02 ...
          - a / 2 * a12 * b01 ...
          - (5 * a) / 4 * a12 * b21 ...
          - (5 * a) / 4 * a21 * b12 ...
          - a * a11 * b22 ...
          - a * a22 * b11 ...
          - 2 * a * a22 * b31 ...
          - 2 * a * a31 * b22 ...
          + 2 * a * a11 * b04;
  dx(10) = - 16 * kappa * b04 ...
           - a * a12 * b12 ...
           - a * a11 * b13 ...
           - a * a13 * b11 ...
           - 2 * a * a22 * b22;
  dx(11) = - (a**2 + 1) * nu * a11 ...
           + (a*c)/(a**2 + 1) * b11 ...
           + (9 * a * (a**2 - 1))/(4 * (a**2 + 1)) * a12 * a21 ...
           + (a * (3 * a**2 - 5))/(a**2 + 1) * a13 * a22 ...
           + (a * (5 * a**2 - 3))/(a**2 + 1) * a22 * a31;
  dx(12) = - (4 * a**2 + 1) * nu * a21 ...
           + (2 * a * c)/(4 * a**2 + 1) * b21 ...
           + (9 * a)/(4 * (4 * a**2 + 1)) * a11 * a12 ...
           + (5 * a * (8 * a**2 - 3))/(4 * (4 * a**2 + 1)) * a12 * a31 ...
           + (25 * a)/(4 * (4 * a**2 + 1)) * a12 * a13;
  dx(13) = - (a**2 + 4) * nu * a12 ...
           + (a * c)/(a**2 + 4) * b12 ...
           - (9 * a**3)/(4 * (a**2 + 4)) * a11 * a21 ...
           + (5 * a * (3 * a**2 - 8))/(4 * (a**2 + 4)) * a13 * a21 ...
           - (25 * a**3)/(4 * (a**2 + 4)) * a21 * a31;
  dx(14) = - (9 * a**2 + 1) * nu * a31 ...
           + (3 * a * c)/(9 * a**2 + 1) * b31 ...
           + (15 * a * (1 - a**2))/(4 * (9 * a**2 + 1)) * a12 * a21 ...
           + (12 * a * (1 + a**2))/(4 * (9 * a**2 + 1)) * a11 * a22 ...
           + (8 * a * (5 - 3 * a**2))/(4 * (9 * a**2 + 1)) * a13 * a22;
  dx(15) = - 4 * (a**2 + 1) * nu * a22 ...
           + (a * c)/(2 * (a**2 + 1)) * b22 ...
           + (2 * a)/(a**2 + 1) * a11 * a13 ...
           - (2 * a**3)/(a**2 + 1) * a11 * a31 ...
           + (4 * a**3)/(a**2 + 1) * a13 * a31 ...
           - (4 * a)/(a**2 + 1) * a13 * a31;
  dx(16) = - (a**2 + 9) * nu * a13 ...
           + (a * c)/(a**2 + 9) * b13 ...
           + (15 * a * (1 - a**2))/(4 * (a**2 + 9)) * a12 * a21 ...
           - (12 * a * (1 + a**2))/(4 * (a**2 + 9)) * a11 * a22 ...
           + (8 * a * (3 - 5 * a**2))/(4 * (a**2 + 9)) * a22 * a31;
  return
end

